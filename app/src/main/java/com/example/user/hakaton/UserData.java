package com.example.user.hakaton;

public class UserData {

    public String email;
    public String telephone;
    public String location;
    public String name;

    public UserData() {}

    public UserData(String email, String telephone, String location, String name) {
        this.email = email;
        this.telephone = telephone;
        this.location = location;
        this.name = name;
    }
}
